"""
Analyst Engine for Project EXODUS.

Rigorously validates or destroys each hypothesis generated by the pipeline.
Takes a hypothesis paired with the relevant observational data, executes the
test method specified by the hypothesis, and produces a multi-dimensional
validation result that either advances the candidate to the Breakthrough Engine
or explains it away as astrophysical or instrumental.

The five validation scores
--------------------------
1. **detection_score** (0-1): Strength of the anomaly actually found in the
   data.  A high value means the signal is loud and clear.
2. **natural_explanation_score** (0-1): Probability that a known astrophysical
   process (variable star, debris disc, stellar contamination, etc.) fully
   explains the anomaly.
3. **instrumental_score** (0-1): Probability that the anomaly is an
   instrumental or data-processing artefact (bad pixel, persistence, RFI,
   systematic).
4. **statistical_significance**: p-value against the null hypothesis of pure
   noise.  Lower is more significant.
5. **reproducibility** (0-1): Does the signal appear in at least one
   independent dataset or observation epoch?

Classification logic
--------------------
    UNEXPLAINED        strong detection AND natural < 0.5 AND instrumental < 0.3
    CONFIRMED_NATURAL  natural explanation >= 0.5
    CONFIRMED_ARTIFACT instrumental score >= 0.5
    INCONCLUSIVE       everything else

Supported test methods (referenced by hypothesis objects):
    ir_excess_comparison       -- Compare IR excess between target and control
    anomaly_detection_lightcurve -- Look for anomalous dimming events
    radio_search_targeted      -- Search BL radio data for narrowband signals
    single_transit_ir_correlation -- Correlate single-transit events with IR
    temporal_differencing      -- Compare radio surveys across decades
"""

from __future__ import annotations

import sys
from dataclasses import dataclass, field, asdict
from datetime import datetime, timezone
from enum import Enum
from typing import Any, Callable, Dict, List, Optional

import numpy as np
from scipy import stats as sp_stats

